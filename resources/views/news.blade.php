            <h2 class="display-6 text-center">PTPN Group dan Suntory Garuda Buka Pasar Baru, Ekspor Perdana Teh
                Oolong Indonesia ke Vietnam</h2>
            <p class="mb-3 text-center">Jakarta, 7 Agustus 2023</p>
            <div class="article-content text-justify">
                <p>PT Perkebunan Nusantara (PTPN) Group bersama PT Suntory Garuda Beverage melakukan ekspor perdana
                    teh Oolong ke Vietnam untuk Suntory Pepsico Vietnam Beverage. Langkah ini menandai awal dari
                    kolaborasi strategis antara kedua korporasi untuk memasok bahan baku berkualitas tinggi bagi
                    minuman ready-to-drink (RTD) di seluruh Asia.</p>

                <p>Kolaborasi Suntory dan PTPN memberi Suntory akses ke perusahaan perkebunan terbesar di Indonesia
                    dengan keunggulan komersial dan operasional. PTPN memperoleh manfaat dari pengetahuan global
                    Suntory dan keahlian sebagai produsen minuman kelas dunia, terutama teh oolong dengan
                    berkomitmen untuk mengikuti metode pertanian berkelanjutan dan mematuhi standar kualitas
                    internasional untuk memastikan keunggulan produk.</p>

                <p>Teh Oolong, yang memiliki karakteristik unik dan cita rasa lembut, dipilih khusus sebagai
                    komoditas ekspor sesuai dengan permintaan pasar. Direktur Pemasaran PTPN Group, Dwi Sutoro,
                    menyampaikan bahwa kerja sama ini merupakan bentuk keseriusan perusahaan untuk menghasilkan
                    produk teh yang diinginkan oleh pembeli. “Ini pengalaman pertama kami memproduksi teh jenis
                    Oolong. Kami produksi karena ada permintaan pasar khusus dan kami memiliki kompetensi untuk
                    mengembangkan semua jenis teh sesuai dengan permintaan pembeli,” ujarnya.</p>

                <p>PTPN Group diketahui mengelola lebih dari 23.000 hektar lahan perkebunan teh di Indonesia. Dengan
                    luas area perkebunan ini, PTPN Group merupakan salah satu perusahaan perkebunan teh terbesar di
                    dunia dengan kapasitas produksi mencapai 50.000 ton per tahun atau 40% dari produksi nasional.
                    Selain teh Oolong, PTPN Group juga menghasilkan produk berupa black tea (orthodox dan CTC),
                    green tea, dan white tea. PTPN Group memiliki beberapa merek dagang produk teh retail seperti
                    Nusakita, Walini, Teh Kayu Aro, Goalpara, Rollas, Kaligua, Gunung Dempo, Semugih, dan Tobasari.
                </p>

                <p>Ekspor teh Oolong ke Vietnam ini akan digunakan oleh Suntory untuk membuat produk minuman
                    ready-to-drink di pasar Asia. Suntory adalah salah satu perusahaan produk konsumen terkemuka di
                    dunia yang didirikan sejak 1899 di Osaka, Jepang, dan memiliki berbagai merek minuman kemasan
                    terkemuka di dunia seperti TEA+, Ribena, dan Okky Jelly Drink.</p>

                <p>CEO Suntory Garuda Beverage, Neeraj Kumar Goyal, menyebutkan bahwa bahan baku yang mereka gunakan
                    harus memenuhi standar kualitas yang tinggi. “Di Suntory, kami selalu berusaha untuk menggunakan
                    cara-cara yang lebih baik dan lebih inovatif untuk memenuhi berbagai cita rasa dan gaya hidup di
                    seluruh dunia,” ungkap Neeraj. ” Kemitraan kami dengan PTPN adalah bagian dari komitmen kami
                    untuk berbagi pengetahuan global kami untuk memastikan produk berkualitas tinggi bagi konsumen,”
                    tambah beliau.</p>

                <p>Kerja sama ekspor jenis teh Oolong ini telah diinisiasi sejak tahun 2021 dengan berbagai tahap
                    persiapan meliputi percobaan, pengujian, dan audit. Teh Oolong produksi PTPN Group telah
                    memenuhi Standar Nasional Indonesia (SNI) dan lolos uji kandungan pestisida dengan 268 bahan
                    aktif yang dipersyaratkan. Selain itu, produk ini juga telah lolos uji kandungan logam berat
                    (Cd, Pb, Sn, Hg, dan As) dan uji kandungan mikrobiologi (TPC, Coliform, Yeast, dan Mould).</p>

                <p>Sebelumnya, produk black tea yang diproduksi oleh PTPN Group juga sudah merambah pasar
                    internasional ke berbagai negara seperti Pakistan, Rusia, Malaysia, Asia Timur, Timur Tengah,
                    Amerika Serikat, dan Eropa. Produk teh Indonesia ini telah memenuhi standar keamanan pangan yang
                    dipersyaratkan oleh masing-masing negara. Produksi jenis green tea juga sedang digalakkan
                    terutama di perkebunan di wilayah Sumatera sesuai permitnaan pasar.</p>

                <p>Pelepasan ekspor perdana ini diharapkan akan membuka peluang bisnis baru bagi PTPN Group,
                    Suntory, dan perusahaan multinasional lainnya, serta membawa manfaat ekonomi bagi negara.
                    Informasi mengenai peluang bisnis teh dengan PTPN Group dapat ditanyakan melalui e-mail
                    tea@holding-perkebunan.com. Selain itu, kolaborasi ini juga diharapkan bisa mendorong promosi
                    produk-produk teh Indonesia yang berkualitas tinggi ke pasar internasional.</p>

